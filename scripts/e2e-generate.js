#!/usr/bin/env node
/**
 * e2e-generate.js — Calls a Claude sub-agent (Sonnet) to generate a Playwright
 * E2E test suite from the TESTING input doc + the manifest contracts.
 *
 * Usage:
 *   node e2e-generate.js \
 *     --input-dir <dir>        # contains TESTING.md (or testing.md / test.md)
 *     --manifest-dir <dir> \
 *     --output-dir <dir> \
 *     --app-url <url>          # base URL for the running app (e.g. http://localhost:3000)
 *     [--model <model>]        # default: claude-sonnet-4-6
 *     [--human-guidance <text>] # operator instruction (e.g. selector-repair re-runs)
 *
 * Writes:
 *   <output-dir>/e2e/tests.spec.ts      — Playwright test file
 *   <output-dir>/e2e/playwright.config.ts — minimal Playwright config
 *   <output-dir>/e2e/page-objects/      — one POM class per major page (if applicable)
 *
 * Returns "ok:tests=<N>" or "err:[description]"
 *
 * NOTE: Requires ANTHROPIC_API_KEY in environment, UNLESS running inside
 *       Claude Code (CLAUDECODE set), where the `claude` CLI provides auth.
 * Uses the same subagent.js call pattern as the rest of HPC (reads stdout only).
 */

const fs   = require("fs");
const path = require("path");
const { execSync } = require("child_process");

// ── CLI args ──────────────────────────────────────────────────────────────

function parseArgs() {
  const args = process.argv.slice(2);
  const parsed = { model: "claude-sonnet-4-6" };
  for (let i = 0; i < args.length; i++) {
    switch (args[i]) {
      case "--input-dir":    parsed.inputDir    = args[++i]; break;
      case "--manifest-dir": parsed.manifestDir = args[++i]; break;
      case "--output-dir":   parsed.outputDir   = args[++i]; break;
      case "--app-url":      parsed.appUrl       = args[++i]; break;
      case "--model":        parsed.model        = args[++i]; break;
      case "--human-guidance": parsed.humanGuidance = args[++i]; break;
    }
  }
  return parsed;
}

// ── File helpers ──────────────────────────────────────────────────────────

const TESTING_NAMES = ["TESTING.md", "testing.md", "test.md", "TEST.md", "e2e.md"];

function findTestingDoc(inputDir) {
  if (!inputDir || !fs.existsSync(inputDir)) return null;
  for (const name of TESTING_NAMES) {
    const p = path.join(inputDir, name);
    if (fs.existsSync(p)) return p;
  }
  return null;
}

function readContracts(manifestDir) {
  const contractsDir = path.join(manifestDir, "contracts");
  if (!fs.existsSync(contractsDir)) return "";
  const lines = [];
  for (const f of fs.readdirSync(contractsDir).filter(f => f.endsWith(".yaml"))) {
    try { lines.push(fs.readFileSync(path.join(contractsDir, f), "utf-8")); }
    catch {}
  }
  return lines.join("\n\n");
}

function readArchitectureYaml(manifestDir) {
  const p = path.join(manifestDir, "architecture.yaml");
  if (!fs.existsSync(p)) return "";
  return fs.readFileSync(p, "utf-8");
}

function readInfraYaml(manifestDir) {
  const p = path.join(manifestDir, "infra-requirements.yaml");
  if (!fs.existsSync(p)) return "";
  return fs.readFileSync(p, "utf-8");
}

// ── Minimal Playwright config ─────────────────────────────────────────────

function buildPlaywrightConfig(appUrl) {
  return `import { defineConfig, devices } from '@playwright/test';

/**
 * Auto-generated by HPC e2e-generate.js — edit as needed.
 * @see https://playwright.dev/docs/test-configuration
 */
export default defineConfig({
  testDir: '.',
  fullyParallel: false,
  retries: 1,
  timeout: 30_000,
  reporter: [
    ['list'],
    ['json', { outputFile: 'e2e-results.json' }],
    ['html', { open: 'never', outputFolder: 'e2e-report' }],
  ],
  use: {
    baseURL: '${appUrl}',
    screenshot: 'only-on-failure',
    video: 'off',
    trace: 'retain-on-failure',
  },
  projects: [
    {
      name: 'chromium',
      use: { ...devices['Desktop Chrome'] },
    },
  ],
});
`;
}

// ── Claude sub-agent call ─────────────────────────────────────────────────

const SUBAGENT_SCRIPT = path.resolve(__dirname, "subagent.js");

/**
 * Call Claude via subagent.js to generate the Playwright test suite.
 * The prompt is written to a temp file; the output is captured and returned.
 */
function callSubagent(prompt, model, outputDir) {
  const tmpPrompt = path.join(outputDir, "_e2e_prompt.txt");
  const tmpOutput = path.join(outputDir, "_e2e_output.ts");
  fs.mkdirSync(outputDir, { recursive: true });
  fs.writeFileSync(tmpPrompt, prompt, "utf-8");

  const cmd = `node "${SUBAGENT_SCRIPT}" --phase freeform --prompt-file "${tmpPrompt}" --output-file "${tmpOutput}" --model "${model}"`;

  try {
    const result = execSync(cmd, { encoding: "utf-8", timeout: 120000 }).trim();
    if (!result.startsWith("ok")) {
      return { ok: false, error: result };
    }
    const output = fs.existsSync(tmpOutput) ? fs.readFileSync(tmpOutput, "utf-8") : "";
    return { ok: true, output };
  } catch (e) {
    return { ok: false, error: e.message.slice(0, 300) };
  } finally {
    try { fs.unlinkSync(tmpPrompt); } catch {}
  }
}

// ── Prompt builder ────────────────────────────────────────────────────────

function buildPrompt({ testingDoc, contracts, architectureYaml, infraYaml, appUrl, humanGuidance }) {
  const guidanceBlock = humanGuidance ? `
## HUMAN GUIDANCE (highest priority)

The operator supplied the following instruction for this run. It takes
precedence over any conflicting requirement above:

${humanGuidance}
` : "";
  return `You are a senior QA engineer generating a Playwright end-to-end test suite.

## Context

### Application Base URL
${appUrl}

### Architecture Overview
\`\`\`yaml
${architectureYaml || "(not available)"}
\`\`\`

### Infrastructure / Emulator Info
\`\`\`yaml
${infraYaml || "(not available)"}
\`\`\`

### API Contracts
\`\`\`yaml
${contracts || "(none defined)"}
\`\`\`

### Testing Specification (TESTING.md)
${testingDoc}

## Instructions

Generate a complete Playwright test file (\`tests.spec.ts\`) covering all acceptance criteria and user flows in the TESTING specification above.

Requirements:
1. Use TypeScript and \`@playwright/test\`.
2. Use the \`baseURL\` from the Playwright config (\`${appUrl}\`) — never hard-code full URLs.
3. Group tests with \`test.describe()\` blocks matching the sections in the TESTING doc.
4. For each scenario, take a screenshot using \`await page.screenshot({ path: 'screenshots/<name>.png', fullPage: true })\`.
5. Use explicit \`await expect\` assertions from \`@playwright/test\`.
6. Seed any required test data by calling API endpoints directly (using \`request\` fixture or \`fetch\`) before each test that needs it, and tear it down in \`afterEach\`.
7. If auth is required, implement a \`loginAs(page, email, password)\` helper and reuse it.
8. Keep each test independent — no shared DOM state between tests.
9. Include a smoke test at the top that hits \`/\` and asserts HTTP 200.
10. Do NOT mock the backend — tests should hit the real emulated infrastructure.
${guidanceBlock}
Output ONLY the TypeScript test file content. No explanations, no markdown fences, no comments outside the file itself.`;
}

// ── Count tests in output ─────────────────────────────────────────────────

function countTests(tsContent) {
  const matches = tsContent.match(/\btest\s*\(/g);
  return matches ? matches.length : 0;
}

// ── Write page objects (heuristic) ───────────────────────────────────────

/**
 * If the test output references a page object class (e.g. LoginPage, NotesPage),
 * write a minimal stub so imports don't break on first run.
 */
function writePageObjectStubs(tsContent, e2eDir) {
  const pomDir = path.join(e2eDir, "page-objects");
  const imports = [...tsContent.matchAll(/from ['"]\.\/page-objects\/(\w+)['"]/g)];
  if (imports.length === 0) return;

  fs.mkdirSync(pomDir, { recursive: true });

  for (const [, className] of imports) {
    const stubPath = path.join(pomDir, `${className}.ts`);
    if (fs.existsSync(stubPath)) continue;

    fs.writeFileSync(stubPath, `import { Page } from '@playwright/test';

/**
 * Auto-generated stub for ${className} — fill in selectors and actions.
 */
export class ${className} {
  constructor(readonly page: Page) {}
}
`);
  }
}

// ── Main ──────────────────────────────────────────────────────────────────

function main() {
  const args = parseArgs();

  if (!args.inputDir || !args.outputDir || !args.appUrl) {
    console.log("err:usage: node e2e-generate.js --input-dir <dir> --manifest-dir <dir> --output-dir <dir> --app-url <url>");
    process.exit(0);
  }

  // Model access goes through subagent.js → the `claude` CLI. Inside Claude Code
  // (CLAUDECODE set) that CLI inherits native auth, so ANTHROPIC_API_KEY is not
  // required; only enforce the key when running outside Claude Code.
  if (!process.env.CLAUDECODE && !process.env.ANTHROPIC_API_KEY) {
    console.log("err:no_api_key: ANTHROPIC_API_KEY is not set (and not running inside Claude Code)");
    process.exit(0);
  }

  const testingDocPath = findTestingDoc(args.inputDir);
  if (!testingDocPath) {
    console.log(`err:testing_doc_not_found: no TESTING.md found in ${args.inputDir}`);
    process.exit(0);
  }

  const testingDoc     = fs.readFileSync(testingDocPath, "utf-8");
  const contracts      = args.manifestDir ? readContracts(args.manifestDir) : "";
  const archYaml       = args.manifestDir ? readArchitectureYaml(args.manifestDir) : "";
  const infraYaml      = args.manifestDir ? readInfraYaml(args.manifestDir) : "";

  const e2eDir = path.join(args.outputDir, "e2e");
  fs.mkdirSync(e2eDir, { recursive: true });

  const screenshotsDir = path.join(args.outputDir, "screenshots");
  fs.mkdirSync(screenshotsDir, { recursive: true });

  // Write Playwright config
  fs.writeFileSync(path.join(e2eDir, "playwright.config.ts"), buildPlaywrightConfig(args.appUrl));

  // Build prompt and call subagent
  const prompt = buildPrompt({
    testingDoc,
    contracts,
    architectureYaml: archYaml,
    infraYaml,
    appUrl: args.appUrl,
    humanGuidance: args.humanGuidance,
  });

  const result = callSubagent(prompt, args.model, args.outputDir);

  if (!result.ok) {
    console.log(`err:subagent_failed:${result.error}`);
    process.exit(0);
  }

  if (!result.output || result.output.trim().length < 50) {
    console.log("err:empty_output: subagent returned no test content");
    process.exit(0);
  }

  // Clean up output: strip accidental markdown fences if any
  let tsContent = result.output
    .replace(/^```(?:typescript|ts)?\n/m, "")
    .replace(/\n```$/m, "")
    .trim();

  // Ensure screenshots path is relative to outputDir
  tsContent = tsContent.replace(
    /screenshots\/([^'".]+\.png)/g,
    (_, name) => `screenshots/${name}`
  );

  // Write the test file
  fs.writeFileSync(path.join(e2eDir, "tests.spec.ts"), tsContent);

  // Write page object stubs if referenced
  writePageObjectStubs(tsContent, e2eDir);

  const testCount = countTests(tsContent);
  console.log(`ok:tests=${testCount}`);
}

main();
